const express = require('express');
const ideasRouter = express.Router();

const {
  getAllFromDatabase,
  addToDatabase,
  getFromDatabaseById,
  updateInstanceInDatabase,
  deleteFromDatabasebyId
} = require('./db');
const checkMillionDollarIdea = require('./checkMillionDollarIdea');

// Database model type constant for ideas.
const IDEAS = 'ideas';

ideasRouter.param('ideaId', (req, res, next, id) => {
  req.ideaId = id;
  next();
});

// Get an array of all ideas.
ideasRouter.get('/', (req, res, next) => {
  const ideas = getAllFromDatabase(IDEAS);

  if (ideas !== null) {
    res.status(200).send(ideas);
  } else {
    res.status(500).send();
  }
});

// Create a new idea and save it to the database.
ideasRouter.post('/', checkMillionDollarIdea, (req, res, next) => {
  const newIdea = req.body;
  const result = addToDatabase(IDEAS, newIdea);

  if (result !== null) {
    res.status(201).send(result);
  } else {
    res.status(500).send();
  }
});

// Get a single idea by id.
ideasRouter.get('/:ideaId', (req, res, next) => {
  const idea = getFromDatabaseById(IDEAS, req.ideaId);

  if (idea) {
    res.status(200).send(idea);
  } else {
    res.status(404).send();
  }
});

// Update a single idea by id.
ideasRouter.put('/:ideaId', checkMillionDollarIdea, (req, res, next) => {
  const updatedIdea = updateInstanceInDatabase(IDEAS, req.body);

  if (updatedIdea) {
    res.status(200).send(updatedIdea);
  } else {
    res.status(404).send();
  }
});

// Delete a single idea by id.
ideasRouter.delete('/:ideaId', (req, res, next) => {
  const ideaId = req.ideaId;
  const ideaDeleted = deleteFromDatabasebyId(IDEAS, ideaId);

  if (ideaDeleted) {
    res.status(204).send();
  } else {
    res.status(404).send();
  }
});

module.exports = ideasRouter;